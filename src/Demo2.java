public class Demo2 {
    //public声明其他的类可以访问这个函数
    //static表示这个方法是静态方法，存储在静态存储区，如果没使用static则会报错，提示main()不存在，没有实例化
    //void表示返回值是无类型的(不需要返回值)
    //String[] args从控制台接受的数据，接受用户输入的参数，
    //主函数保证独立运行，程序入口，被jvm调用
    public static void main(String[] args) {
        System.out.println("");
        /*
        注释
        System.out.println("");
         */
        /**
         *
         * System.out.println("");
         *常量
         * 整数常量；小数常量；布尔型常量true,false；字符常量''一个字符；字符串常量"";null常量
         * 整数三种表现形式：十进制，八进制（0开头），十六进制（0x开头）
         */
    }
}
